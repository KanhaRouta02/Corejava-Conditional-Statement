package D_in.kanha.switchstatement;

public class Demo {

	public static void main(String[] args) {
		int a = 20;
		switch (a) {

		case 1: {
			System.out.println("Case - 1");
			break;
		}
		case 2: {
			System.out.println("Case - 2");
			break;
		}
		case 3: {
			System.out.println("Case - 3");
			break;
		}
		default: {
			System.out.println("invalid input..");
		}
		}
	}
}
